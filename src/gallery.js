document.querySelectorAll(".gallery-imgs").forEach((gal) => {
  const winId = gal.closest(".window").id;

  if (!galleries[winId]) return;

  galleries[winId].forEach((photo) => {
    const container = document.createElement("div");
    container.classList.add("gal-object");

    const img = document.createElement("img");
    img.src = `assets/images/${winId}/${photo.img}`;

    container.appendChild(img);

    if (photo.date) {
      const date = document.createElement("span");
      date.classList.add("img-date");
      date.innerText = photo.date;
      container.appendChild(date);
    }

    if (photo.caption) {
      const caption = document.createElement("span");
      caption.classList.add("img-caption");
      caption.innerText = photo.caption;
      container.appendChild(caption);
    }

    gal.appendChild(container);
  });
});
